// The types of room.
// Every room is defined by 6 bytes:
// Byte 0: The width of the interior
// Byte 1: The height of the interior
// Byte 2-3: Offset to points data (Really it is a word)
// Byte 4-5: Offset to line data (Really it is a word)
// The address where the room type is defined is: 0xA982
// The way the points and lines should be read is like:
//	do
//		read lineto_entry (first point defined in bytes 4 - 5)
//		if lineto_entry == 0xFF then exit (no more lines to read)
//		startpoint = points [lineto_entry] 
//					(what it has been read is a relative position with in points table)
//		do
//			read lineto_entry (the next)
//			if lineto_entry == 0xFF then exit (same testing)
//			endpoint = points [lineto_entry] (
//			drawline is startpoint to endpoint
//		loop
//	loop
// This has been extracted from the original source code
// It can be downloaded from: http://www.icemark.com/dataformats/aticatac/
